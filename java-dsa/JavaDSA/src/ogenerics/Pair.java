package ogenerics;

public class Pair {

	private int first;
	private int second;
	
	public Pair(int first,int second) {
		this.first = first;
		this.second = second;
	}
	
	public int getFirst() {
		return this.first;
	}
	
	public int getSecond() {
		return this.second;

	}
	
	public void setFirst(int fir) {
		this.first = fir;
	}
	
	public void setSecond(int sec) {
		this.second = sec;
	}
	
}
